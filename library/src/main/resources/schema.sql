DROP TABLE IF EXISTS users;
CREATE TABLE users (
    id LONG AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    age INT,
    email VARCHAR(255) PRIMARY KEY,
    password VARCHAR(255) NOT NULL
);